package com.example.cw1601java;

public class PositionPoints {
    private int position;
    private int points;

    public PositionPoints(int position, int points) {
        this.position = position;
        this.points = points;
    }

    public int getPosition() {
        return position;
    }

    public int getPoints() {
        return points;
    }
}